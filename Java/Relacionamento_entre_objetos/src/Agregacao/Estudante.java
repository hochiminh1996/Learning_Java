package Agregacao;

public class Estudante {
	private String name;
	private String curso;

	public Estudante() {

	}

	public Estudante(String name, String curso) {
		this.name = name;
		this.curso = curso;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCurso() {
		return curso;
	}

	public void setCurso(String curso) {
		this.curso = curso;
	}
	
	

}
