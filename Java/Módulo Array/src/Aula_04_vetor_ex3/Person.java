package Aula_04_vetor_ex3;

public class Person {
	private String name;
	private double altura;
	private int idade;

	public Person() {

	}

	public Person(String name, double altura, int idade) {
		this.name = name;
		this.altura = altura;
		this.idade = idade;
	}

	public String getName() {
		return name;
	}

	public int getIdade() {
		return idade;
	}

	public double getAltura() {
		return altura;
	}


	
}
